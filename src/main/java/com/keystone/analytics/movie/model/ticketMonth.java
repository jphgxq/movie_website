package com.keystone.analytics.movie.model;

public class ticketMonth {
	
	int rank;
	String name;
	int days;
	int tickets_month;
	int price_ave;
	int people_ave;
	String per_month;
	String release_time;
	String wom_index;
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDays() {
		return days;
	}
	public void setDays(int days) {
		this.days = days;
	}
	public int getTickets_month() {
		return tickets_month;
	}
	public void setTickets_month(int tickets_month) {
		this.tickets_month = tickets_month;
	}
	public int getPrice_ave() {
		return price_ave;
	}
	public void setPrice_ave(int price_ave) {
		this.price_ave = price_ave;
	}
	public int getPeople_ave() {
		return people_ave;
	}
	public void setPeople_ave(int people_ave) {
		this.people_ave = people_ave;
	}
	public String getPer_month() {
		return per_month;
	}
	public void setPer_month(String per_month) {
		this.per_month = per_month;
	}
	public String getRelease_time() {
		return release_time;
	}
	public void setRelease_time(String release_time) {
		this.release_time = release_time;
	}
	public String getWom_index() {
		return wom_index;
	}
	public void setWom_index(String wom_index) {
		this.wom_index = wom_index;
	}

}
