package com.keystone.analytics.movie.model;

public class getTickets {
	String date;
	int id;
	String name;
	String real_time_tickets;
	String ticket_percent;
	String sum_tickets;
	String display_percent;
	int days;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getReal_time_tickets() {
		return real_time_tickets;
	}
	public void setReal_time_tickets(String real_time_tickets) {
		this.real_time_tickets = real_time_tickets;
	}
	public String getTicket_percent() {
		return ticket_percent;
	}
	public void setTicket_percent(String ticket_percent) {
		this.ticket_percent = ticket_percent;
	}
	public String getSum_tickets() {
		return sum_tickets;
	}
	public void setSum_tickets(String sum_tickets) {
		this.sum_tickets = sum_tickets;
	}
	public String getDisplay_percent() {
		return display_percent;
	}
	public void setDisplay_percent(String display_percent) {
		this.display_percent = display_percent;
	}
	public int getDays() {
		return days;
	}
	public void setDays(int days) {
		this.days = days;
	}
	
}
