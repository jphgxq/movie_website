package com.keystone.analytics.movie.model;

public class ticketHistory {
	
	String name;
	String type;
	int year;
	int sum_tickets;
	int price_ave;
	int people_ave;
	String area;
	String date;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getSum_tickets() {
		return sum_tickets;
	}
	public void setSum_tickets(int sum_tickets) {
		this.sum_tickets = sum_tickets;
	}
	public int getPrice_ave() {
		return price_ave;
	}
	public void setPrice_ave(int price_ave) {
		this.price_ave = price_ave;
	}
	public int getPeople_ave() {
		return people_ave;
	}
	public void setPeople_ave(int people_ave) {
		this.people_ave = people_ave;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}
